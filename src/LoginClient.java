    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        if(initialize) return;    
        try{
                DatagramSocket socket = new DatagramSocket();
                String host =  txtEndereco.getText();
		String port = txtPorta.getText();
		String name = txtNome.getText();
                dialogue = new ClientInterfaceGrafica(socket, host, port, name);
                
                MessageReceiver r = new MessageReceiver(socket, this);
		Thread rt = new Thread(r);
		rt.start();
                
                java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                        dialogue.setVisible(true);
                        dialogue.setTxtMensagem("OI");
                        dialogue.setClickBtnEnviar();
                    }
                });
                initialize = true;
                this.txtStatus.setText("Sucesso");
            }catch(Exception e){
                this.txtStatus.setText(e.getMessage());
            }
    }//GEN-LAST:event_btnEntrarActionPerformed

